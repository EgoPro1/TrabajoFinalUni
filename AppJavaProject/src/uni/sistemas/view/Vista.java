        n = txtApellido.getText();
        e.setApellido(n);

        int num = cmbC.getSelectedIndex();
        e.setCargo(Controller.Cargo(num));

       if (rbt1.isSelected()) {
            n = rbt1.getText();
        }
        if (rbt2.isSelected()) {
            n = rbt2.getText();
        }
        if (rbt3.isSelected()) {
            n = rbt3.getText();
        }
        e.setArea(n);
        Double num2 = Double.parseDouble(txtS.getText());
