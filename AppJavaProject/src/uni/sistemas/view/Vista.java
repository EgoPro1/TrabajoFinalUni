    private void procesar5() throws IOException {
        FileWriter fw;
        FileReader fr;
        File f;
        PrintWriter pw;

        String archivo = "Empleados.txt";
        f = new File(archivo);
        if (f.exists()) {
            f.delete();
        }

        try {
            fw = new FileWriter(archivo, true);

            pw = new PrintWriter(fw);

            for (Empleado em : control.EmpleadoMostrar2()) {

                pw.write(em.toString());
            }

            pw.close();

        } catch (IOException ex) {
            throw ex;
        } finally {

        }

    }

